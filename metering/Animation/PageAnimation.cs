namespace metering
{
    /// <summary>
    /// Styles of page animations
    /// </summary>
    public enum PageAnimation
    {
        /// <summary>
        /// No animation takes place
        /// </summary>
        None = 0,

        /// <summary>
        /// The page slides in and fades in from right
        /// </summary>
        SlideAndFadeInFromRight = 1,

        /// <summary>
        /// The page slides out and fades out to left
        /// </summary>
        SlideAndFadeOutToRight = 2,
    }
}
